age = 15

if age < 2:
    print('Você é um bebê. \n')
elif age < 4:
    print('Você é uma criança \n')
elif age < 13:
    print('Vocë é um(a) Gatoro(a) \n')
elif age < 20:
    print('Você é um(a) adolescente \n')
elif age < 65:
    print('Você é um adulto \n')
elif age >= 65:
    print('Você é um idoso \n')

